GO

USE master;
GO 


BACKUP CERTIFICATE MyCert TO 
	FILE = 'D:\certificate.cer'  
WITH PRIVATE KEY ( 
	FILE = 'D:\certificate.pvk' ,   
    ENCRYPTION BY PASSWORD = 'bmcsdl'
);  
GO  

