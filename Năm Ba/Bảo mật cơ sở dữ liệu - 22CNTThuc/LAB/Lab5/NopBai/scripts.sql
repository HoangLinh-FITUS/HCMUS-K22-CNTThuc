GO

CREATE CERTIFICATE MyCert
WITH SUBJECT = 'My Cert';
GO

USE QLBongDa;
GO

CREATE DATABASE ENCRYPTION KEY
WITH ALGORITHM = AES_256
ENCRYPTION BY SERVER CERTIFICATE MyCert;
GO

ALTER DATABASE QLBongDa
